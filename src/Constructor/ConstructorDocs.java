package Constructor;

public class ConstructorDocs {

	/*
	 *  ================= Constructor ===================
	 *  
	 *  # used to create object in java
	 *  # special type of method which has same name as that of class
					  
				Syntax:
					   
					   class_name(){
					    
					    // statements
					     
					    }
	 *  
	 *  # types of constructor
	 *  
	 *  	i. default constructor
	 * 			# used to create object with its default values
	 * 			
	 * 			syntax:
	 * 					class_name(){
	 * 
	 * 					// set default values to object
	 * 
	 * 					}
	 *  	   
	 *  
	 *  	ii. parameterized constructor
	 * 			# used to create object with its dynamic values (values passed through input)
	 * 
	 * 			syntax:
	 * 					class_name(arg1, arg2, ......, argn){
	 * 
	 * 						// statements
	 * 
	 * 					}
	 * 
	 * 
	 * 
	 * 
	 * 
	 */
	
}
