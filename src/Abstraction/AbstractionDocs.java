package Abstraction;

public class AbstractionDocs {
	/*
	 * ========================= Abstraction =========================
	 * 
	 * # process of hiding implementation details in the program is known as abstraction.
	 * 
	 *  # to achieve abstraction
	 *  
	 *  		a. abstraction class:
	 *  
	 *  			# class which contains abstract and non-abstract methods
	 *  
	 *  				- abstract method:
	 *  					# method which does not have implementation details or body.
	 *  
	 *  						e.g:
	 *  							abstract void add();
	 *  
	 *  			# we cannot create object of abstract class.
	 *  
	 *  			syntax:
	 *  					abstract class class_name(){
	 *  
	 *  					// abstract methods
	 *  
	 *  					// non abstract methods
	 *  
	 *  					}				
	 * 			b. Interface
	 * 			
	 * 				# same as class which contains public abstract methods only.
	 * 				# we cannot create objects of interface .
	 * 
	 * 				syntax:
	 * 						interface interface_name(){
	 * 				
	 * 						// public static final variables only
	 * 
	 * 						// public abstract methods only
	 * 
	 * 						}
	 * 
	 * 
	 * 
	 * 
	 * 
	 * 
	 */
}
